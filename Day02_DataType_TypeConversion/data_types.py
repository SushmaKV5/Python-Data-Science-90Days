#Demonstration of Python data types

int_val = 10
float_val = 3.14
bool_val = True
str_val = "Python"
list_val = [1, 2, 3]
tuple_val = (1, 2, 3)
set_val = {"Hello", 1, 3.14, (1,2), 54}
dict_val = {"name": "Sushma", "age": 21}

print("Integer:" , int_val, type(int_val))
print("Float:", float_val, type(float_val))
print("Boolean:", bool_val, type(bool_val))
print("String:", str_val, type(str_val))
print("List:", list_val, type(list_val))
print("tuple:", tuple_val, type(tuple_val))
print("Set:", set_val, type(set_val))
print("Dictionary:", dict_val, type(dict_val))